package sec02.exam02;

public class StringExample {

	public static void main(String[] args) {
		String name = "홍길동";
		String job = "프로게이머";
		System.out.println(name);
		System.out.println(job);
		System.out.println("\n제이름은 " + name + "이고, 직업은 \"" + job + "\" 입니다");
		// \n -> 개행
		// \t -> tab만큼 띄우기
		// \" -> 문자열에 " 사용
	}

}
